package lesson3one;

public class AssignmentOperators 
{

	public static void main(String[] args) 
	{
      int a =2;
      int b=5;
      int c=8;
      
      a+=4;
      b*=2;
      c+=a*b;
      c%=6;
      System.out.println("a="+a);
      System.out.println("b="+b);
      System.out.println("c="+c);
      
	}

}
