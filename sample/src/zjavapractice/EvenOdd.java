package zjavapractice;

public class EvenOdd 
{
	static void  checkEvenOdd() 
	{
		int a = 11; 
	
		if(a % 2 == 0) {
			System.out.println(a+" is even number ");
		}
		else {
			System.out.println(a+" is Odd number ");
		}
		
	}

	public static void main(String[] args) 
	{
	
		checkEvenOdd();

	}

}
