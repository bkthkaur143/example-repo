package lesson3;

public class Switch {

	public static void main(String[] args) {
		int i=3;
		switch(i) {
		case 0:
			System.out.println("i is 0");
			break;
		case 1:
			System.out.println("i is 1");
			break;
		case 2:
			System.out.println("i is 2");
			break;
		default :
			System.out.println("ivalid");
		}

	}

}
